    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        String firstName = txtFirstName.getText().trim();
        String lastName = txtLastName.getText().trim();
        String username = txtUsername.getText().trim();
        String password = new String(txtPassword.getPassword()).trim();
        String cellphone = txtCellphoneNumber.getText().trim();
        
      if (firstName.isEmpty() || lastName.isEmpty() || password.isEmpty() || password.isEmpty() || cellphone.isEmpty()) {
          JOptionPane.showMessageDialog(this,  "Please fill in all fields.", "Missing Info", JOptionPane.WARNING_MESSAGE);
          return;
      
      }
        
        
        
        if (!checkUsername(username)) {
            JOptionPane.showMessageDialog(this, "Username is not correctly formatted please ensure that username contains an underscore and is no more than five characters in length.");
            return;
        }
        
        if (!checkPasswordComplexity(password)) {
            JOptionPane.showMessageDialog(this, "Password is not correctly formatted; please ensure that the password contains atleast eight characters, include a capital letter and special character.");
            return;
        }
        
        if (!checkCellphoneNumber(cellphone)) {
            JOptionPane.showMessageDialog(this, "Phone number incorrectly formatted or does not contain international code.");
            return;
        }
        
        try {
            Connection conn = DBConnection.getConnection();
            if (conn != null) {
                String sql = "INSERT INTO users (first_name, last_name, username, password, cellphone) VALUES (?, ?, ?, ?, ?)";
                java.sql.PreparedStatement pst = conn.prepareStatement(sql);
                pst.setString(1, firstName);
                pst.setString(2, lastName);
                pst.setString(3, username);
                pst.setString(4, password);
                pst.setString(5, cellphone);
                
                int rowsInserted = pst.executeUpdate();
                if (rowsInserted > 0) {
                JOptionPane.showMessageDialog(this, "Username successfully captured, "
                        + "Password successfully captured, "
                        + "Cellphone number successfully added, "
                        + "Registration successful! You can log in.");
                new LoginForm().setVisible(true);
                this.dispose();
            } else {
                    JOptionPane.showMessageDialog(this, "Registration failed. Please try again.");      
                    }
            conn.close();
            }
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Database error: " + ex.getMessage());
        }
            
      
    }//GEN-LAST:event_btnRegisterActionPerformed

